package br.ufpa.labes.spm.exceptions;

public class ImplementationException extends WebapseeException {

  /** Computed serialVersionUID */
  private static final long serialVersionUID = 3767790387207625833L;

  public ImplementationException() {
    super();
  }

  public ImplementationException(String arg0) {
    super(arg0);
  }

  public ImplementationException(String arg0, Throwable arg1) {
    super(arg0, arg1);
  }

  public ImplementationException(Throwable arg0) {
    super(arg0);
  }
}
