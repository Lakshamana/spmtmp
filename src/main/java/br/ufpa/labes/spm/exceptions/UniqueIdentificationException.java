package br.ufpa.labes.spm.exceptions;

public class UniqueIdentificationException extends DataBaseException {

  public UniqueIdentificationException() {
    super();
  }

  public UniqueIdentificationException(String arg0) {
    super(arg0);
  }

  public UniqueIdentificationException(String arg0, Throwable arg1) {
    super(arg0, arg1);
  }

  public UniqueIdentificationException(Throwable arg0) {
    super(arg0);
  }
}
