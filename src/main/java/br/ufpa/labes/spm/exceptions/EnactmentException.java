/** @author breno */
public class EnactmentException extends WebapseeException {

  public EnactmentException() {
    super();
  }

  public EnactmentException(String arg0) {
    super(arg0);
  }

  public EnactmentException(String arg0, Throwable arg1) {
    super(arg0, arg1);
  }

  public EnactmentException(Throwable arg0) {
    super(arg0);
  }
}
